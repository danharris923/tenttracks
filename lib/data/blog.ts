// Helper functions for blog data
export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogData.find(post => post.slug === slug)
}

export function getFeaturedPosts(limit: number = 3): BlogPost[] {
  // Sort by date and return most recent
  return [...blogData]
    .sort((a, b) => new Date(b.datePublished).getTime() - new Date(a.datePublished).getTime())
    .slice(0, limit)
}

export function getPostsByTag(tag: string): BlogPost[] {
  return blogData.filter(post => 
    post.tags.some(t => t.toLowerCase() === tag.toLowerCase())
  )
}

export function getAllTags(): string[] {
  const allTags = blogData.flatMap(post => post.tags)
  return Array.from(new Set(allTags)).sort()
}

export function searchPosts(query: string): BlogPost[] {
  const searchTerms = query.toLowerCase().split(/\s+/)
  return blogData.filter(post => {
    const searchText = [
      post.title,
      post.description,
      post.author,
      ...post.tags,
    ].join(' ').toLowerCase()

    return searchTerms.every(term => searchText.includes(term))
  })
}

export function getRelatedPosts(post: BlogPost, limit: number = 3): BlogPost[] {
  return blogData
    .filter(p => p.slug !== post.slug)
    .map(p => ({
      post: p,
      score: calculateSimilarity(post, p)
    }))
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(item => item.post)
}

function calculateSimilarity(a: BlogPost, b: BlogPost): number {
  let score = 0
  
  // Common tags (high weight)
  const commonTags = a.tags.filter(tag => b.tags.includes(tag))
  score += commonTags.length * 3
  
  // Same author
  if (a.author === b.author) score += 2
  
  // Similar reading time
  const timeDiff = Math.abs(a.readingTime - b.readingTime)
  score += Math.max(0, 3 - timeDiff / 2)
  
  return score
}